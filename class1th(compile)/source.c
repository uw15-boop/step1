#include<stdio.h>
//전처리기:프로그램이 컴파일되기 이전에 프로그램에 대한 사전 처리를 하는 과정입니다.

// main() : 프로그램의 진입점 역활을 수행하는 함수입니다.
void main()
{
	//; (세미 콜론) : 한 문장의 끝을 알려주는 기호입니다.
	printf("Hello World\n");

	// \n (개행 문자) : 해당하는 위치를 한 칸 아래로 내려주는 문자입니다.  
	printf("등푸른고등어");

	// 전처리기는 컴파일러가 아닌 선행처리기에 의해서 처리되는 문자이기 떄문에
	//명령문 끝에 ";"을 사용하지 않습니다.
}